// Greet a user by name
function greet(name) {
  return "Hello, " + name + "!";
}


// Add two numbers
function add(a, b) {
  return a + b;
}


// Check if a number is even
function isEven(num) {
  return num % 2 === 0;
}


// Output the square of a number
function square(x) {
  return x * x;
}


// Return the smallest value of two numbers
function min(a, b) {
  return Math.min(a, b);
}